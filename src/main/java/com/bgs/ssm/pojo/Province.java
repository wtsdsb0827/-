package com.bgs.ssm.pojo;
/*
    地区表
 */
public class Province {

    private Integer  provinceId;
    private String  provincePro;
    private String  provinceCity;
    private String  provinceArea;


    public Integer getProvinceId() {
        return provinceId;
    }

    public void setProvinceId(Integer provinceId) {
        this.provinceId = provinceId;
    }

    public String getProvincePro() {
        return provincePro;
    }

    public void setProvincePro(String provincePro) {
        this.provincePro = provincePro;
    }

    public String getProvinceCity() {
        return provinceCity;
    }

    public void setProvinceCity(String provinceCity) {
        this.provinceCity = provinceCity;
    }

    public String getProvinceArea() {
        return provinceArea;
    }

    public void setProvinceArea(String provinceArea) {
        this.provinceArea = provinceArea;
    }
}
